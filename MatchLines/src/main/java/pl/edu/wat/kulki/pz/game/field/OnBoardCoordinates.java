package pl.edu.wat.kulki.pz.game.field;

public class OnBoardCoordinates {
    private int x;
    private int y;

    public OnBoardCoordinates(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
